package snakeAndLadder;

public class Snake {

	private String snakeId;
	
	private Integer headPos;
	
	private Integer tailPos;

	public String getSnakeId() {
		return snakeId;
	}

	public void setSnakeId(String snakeId) {
		this.snakeId = snakeId;
	}

	public Integer getHeadPos() {
		return headPos;
	}

	public void setHeadPos(Integer headPos) {
		this.headPos = headPos;
	}

	public Integer getTailPos() {
		return tailPos;
	}

	public void setTailPos(Integer tailPos) {
		this.tailPos = tailPos;
	}
	
	
}
